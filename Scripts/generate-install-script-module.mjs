#!/usr/bin/env node

import { createHash } from 'node:crypto';
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const sourcePath = path.join(repoRoot, 'Scripts', 'install.sh');
const outputPath = path.join(repoRoot, 'Frontend', 'install-script.generated.js');

const installScriptContent = readFileSync(sourcePath, 'utf8');
const installScriptSha256 = createHash('sha256')
  .update(installScriptContent, 'utf8')
  .digest('hex');

const generatedSource = `/* AUTO-GENERATED FILE. DO NOT EDIT.
 * Source: Scripts/install.sh
 * Generated by: Scripts/generate-install-script-module.mjs
 */
export const INSTALL_SCRIPT_SHA256 = '${installScriptSha256}';
export const INSTALL_SCRIPT_CONTENT = ${JSON.stringify(installScriptContent)};
`;

writeFileSync(outputPath, generatedSource, 'utf8');
console.log(`Generated ${path.relative(repoRoot, outputPath)} from ${path.relative(repoRoot, sourcePath)}`);
